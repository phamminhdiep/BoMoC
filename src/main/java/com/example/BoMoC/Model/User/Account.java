package com.example.BoMoC.Model.User;
import com.example.BoMoC.Model.User.User;

public class Account {
	private int id;
	private String username;
	private String password;
	public User _unnamed_User_;


	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}

	public String getUsername() {
		return username;
	}

	public void setUsername(String username) {
		this.username = username;
	}

	public String getPassword() {
		return password;
	}

	public void setPassword(String password) {
		this.password = password;
	}

	public User get_unnamed_User_() {
		return _unnamed_User_;
	}

	public void set_unnamed_User_(User _unnamed_User_) {
		this._unnamed_User_ = _unnamed_User_;
	}
}